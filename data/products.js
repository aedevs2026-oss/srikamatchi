export function getProductBySlug(slug) {
  return products.find((product) => product.slug === slug);
}

export function getRelatedProducts(product) {
  if (!product?.related?.length) return [];
  return product.related
    .map((id) => products.find((item) => item.id === id))
    .filter(Boolean);
}

export function getFeaturedProducts() {
  return products.filter((product) => product.featured);
}

export function getProductsByCategory(category) {
  return products.filter((product) => product.category === category);
}

export function getOfferProducts() {
  return products.filter((product) => product.category !== "furniture");
}

export function getFurnitureProducts() {
  return products.filter(
    (product) =>
      product.category === "furniture" ||
      product.tags?.includes("marriage") ||
      product.tags?.includes("deepavali")
  );
}

export function searchProducts(query) {
  const q = query.trim().toLowerCase();
  if (!q) return products;
  return products.filter((product) => {
    const haystack = [
      product.name,
      product.category,
      product.offer,
      product.description,
      ...(product.tags || []),
      ...(product.features || []),
    ]
      .join(" ")
      .toLowerCase();
    return haystack.includes(q);
  });
}
